const mongoose = require("mongoose")

const DataSchema = new mongoose.Schema({
    username: {
        type: String,
        required: true,
        unique: true
    },
    email: {
        type: String,
        required: true,
        unique: true
    },
    password: {
        type: String,
        required: true
    }
})

const Register = new mongoose.model("users", DataSchema)

module.exports = Register